def greet(name, greeting="你好", punctuation="！"):
    """打印问候语
    考点：默认参数的使用，关键字参数的混合调用
    """
    # 调整了输出格式，使其更符合中文语境
    print(f"{greeting}，{name}{punctuation}")


def calculate_discount_price(price, discount=0.1, tax=0.0):
    """计算折扣后的价格（考虑税费）
    考点：默认参数的计算逻辑
    改进：返回计算结果而非直接打印，提高函数复用性
    """
    discounted_price = price * (1 - discount)
    final_price = discounted_price * (1 + tax)
    return final_price


def create_student(name, age, grade="未定", city="未知"):
    """创建学生信息字典
    考点：默认参数在字典构建中的应用
    改进：返回字典对象
    """
    student = {
        "姓名": name,
        "年龄": age,
        "年级": grade,
        "城市": city
    }
    return student


def print_info(title, items, separator=", ", end="\n"):
    """格式化打印信息
    考点：关键字参数控制打印格式
    修复：修复了原代码中遍历逻辑的Bug，使用enumerate处理索引
    """
    # 先打印标题，通常标题后加冒号或换行，这里保留你的逻辑并稍作优化
    print(f"{title}", end="：")

    # 优化
    # print(*items, sep=separator, end=end)

    # 使用enumerate获取索引和值，避免对key/value结构假设错误
    for index, item in enumerate(items):
        # 如果不是最后一个元素，就加分隔符；否则加结束符
        if index < len(items) - 1:
            print(item, end=separator)
        else:
            print(item, end=end)


